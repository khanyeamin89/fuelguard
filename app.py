# --- ৩. পপ-আপ নির্দেশিকা (Enhanced & Informative) ---
@st.dialog("📖 FuelGuard Pro: ব্যবহারকারী নির্দেশিকা")
def show_instruction():
    st.markdown("""
    ### **স্বাগতম! FuelGuard Pro ব্যবহারের নিয়মাবলী:**
    
    ---
    #### **১. ব্যবহারকারীর ধরণ (Categories)**
    * **🚜 কৃষক (Farmer):** নিবন্ধনের জন্য **NID** এবং **UNO সার্টিফিকেট** নম্বর প্রয়োজন। সেচ কাজের জন্য আপনারা যেকোনো সময় তেল নিতে পারবেন।
    * **🚑 সরকারি (Govt):** জরুরি সেবার যানবাহনের জন্য **Office ID** প্রয়োজন। আপনাদের জন্য কোনো সময়ের সীমাবদ্ধতা (Lock) নেই।
    * **🏍️ সাধারণ (General):** সাধারণ রাইডারদের নিবন্ধনের জন্য সঠিক **গাড়ির নাম্বার** প্রদান বাধ্যতামূলক। 
    
    #### **২. রিফিল এবং লক (Refill Rules)**
    * **৭২ ঘণ্টা নিয়ম:** সাধারণ রাইডাররা একবার তেল নেওয়ার পর পরবর্তী **৭২ ঘণ্টা** পর্যন্ত পুনরায় তেল নিতে পারবেন না।
    * **ব্যতিক্রম:** কৃষক এবং সরকারি যানবাহনের ক্ষেত্রে এই লক প্রযোজ্য নয়।
    
    #### **৩. অপারেটর প্যানেল (Operators)**
    * অপারেটরদের প্রতিদিনের **সিক্রেট পিন** ব্যবহার করে লগইন করতে হবে।
    * তেল দেওয়ার সময় চালকের ছবি তোলা **ঐচ্ছিক**, তবে তথ্য সংরক্ষণ করা বাধ্যতামূলক।
    
    ---
    💡 *সঠিক তথ্য প্রদান করে জ্বালানি ব্যবস্থাপনায় সহায়তা করুন।*
    """)
    
    if st.button("বুঝেছি, অ্যাপে প্রবেশ করুন", use_container_width=True, type="primary"):
        st.session_state.seen_instruction = True
        st.rerun()

# অ্যাপ লোড হওয়ার সময় নির্দেশিকা দেখানো
if "seen_instruction" not in st.session_state:
    show_instruction()
